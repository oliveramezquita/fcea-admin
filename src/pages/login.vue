const authThemeImg = useGenerateImageVariant(authV2LoginIllustrationLight, authV2LoginIllustrationDark, authV2LoginIllustrationBorderedLight, authV2LoginIllustrationBorderedDark, true)
const authThemeMask = useGenerateImageVariant(authV2MaskLight, authV2MaskDark)

definePage({
  meta: {
    layout: 'blank',
    unauthenticatedOnly: true,
  },
})

const isPasswordVisible = ref(false)
const route = useRoute()
const router = useRouter()
const ability = useAbility()

const errors = ref({
  email: undefined,
  password: undefined,
})

const refVForm = ref()

const credentials = ref({
  email: '',
  password: '',
})

const rememberMe = ref(false)

const isAlertVisible = ref(false)
const alertType = ref() 
const alertMessage = ref()
const login = async () => {
  try {
    const res = await $api('api/login', {
      method: 'POST',
      body: {
        email: credentials.value.email,
        password: credentials.value.password,
      },
      onResponseError({ response }) {
        alertType.value = "warning"
        if (response.status === 403) {
          alertMessage.value = "Correo electr贸nico o contrase帽a incorrectos"
        } else if (response.status === 400) {
          alertMessage.value = response._data.length > 0 ? response._data[0] : "Ocurri贸 un error al momento de iniciar sesi贸n"
        } else {
          alertType.value = "error"
          alertMessage.value = `Ocurri贸 un error al momento de asignar la informaci贸n: ${response?._data?.message}`
        }
        isAlertVisible.value = true
      },
    })

    const { accessToken, userData, userAbilityRules } = res

    useCookie('userAbilityRules').value = userAbilityRules
    ability.update(userAbilityRules)
    useCookie('userData').value = userData
    useCookie('accessToken').value = accessToken
    await nextTick(() => {
      router.replace(route.query.to ? String(route.query.to) : '/')
    })
  } catch (err) {
    console.error(err)
  }
}

const onSubmit = () => {
  refVForm.value?.validate().then(({ valid: isValid }) => {
    if (isValid)
      login()
  })
}
</script>

